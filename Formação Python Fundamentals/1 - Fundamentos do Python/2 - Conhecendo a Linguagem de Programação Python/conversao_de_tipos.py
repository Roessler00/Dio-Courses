preco = 10
print(preco)

# Int para Float
preco = float(preco)
print(preco)
preco = 10 / 2
print(preco)

# Float para Int
preco = 10.30
print(preco)
preco = int(preco)
print(preco)

# Conversão por Divisão
preco = 10
print(preco)
print(preco / 2)
print(preco // 2)

# Numérico para String
preco = 10.50
idade = 25
print(str(preco))
print(str(idade))
texto = f"idade {idade} preco {preco}"
print(texto)

# String para Numérico
preco = "10.50"
idade = "28"
print(float(preco))
print(float(idade))

