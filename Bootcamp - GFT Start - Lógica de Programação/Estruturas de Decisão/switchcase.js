// Estrutura de Decisão
// Switch/case/break/default

let fruta = "banana";

switch (fruta) {
  case "laranja":
    console.log("Suco de Laranja");

  case "banana": // Vai executar os 2 cases "banana" e "maça"
    console.log("Vitamina de Banana");

  case "maça":
    console.log("Suco de Maçã");
}
